"""[Midterm] ProgressiveTax"""
def main():
    """function tax"""
    money = int(input())
    tax_5 = int((money-150000)*5/100)
    tax_10 = int(((money-300000)*10/100)+(150000*5/100))
    tax_15 = int(((money-500000)*15/100)+(200000*10/100)+(150000*5/100))
    tax_20 = int(((money-750000)*20/100)+(250000*15/100)+(200000*10/100)+(150000*5/100))
    tax_25 = int(((money-1000000)*25/100)+(250000*20/100)+(250000*15/100)+(200000*10/100)+\
        (150000*5/100))
    tax_30 = int(((money-2000000)*30/100)+(1000000*25/100)+(250000*20/100)+(250000*15/100)+\
        (200000*10/100)+(150000*5/100))
    tax_35 = int(((money-4000000)*35/100)+(2000000*30/100)+(1000000*25/100)+(250000*20/100)+\
        (250000*15/100)+(200000*10/100)+(150000*5/100))
    if 0 <= money <= 150000:
        print("0")
    elif 150000 < money <= 300000:
        print(tax_5)
    elif 300000 < money <= 500000:
        print(tax_10)
    elif 500000 < money <= 750000:
        print(tax_15)
    elif 750000 < money <= 1000000:
        print(tax_20)
    elif 1000000 < money <= 2000000:
        print(tax_25)
    elif 2000000 < money <= 4000000:
        print(tax_30)
    elif 4000000 < money:
        print(tax_35)
main()
